'''Dado o tamanho do lado de um quadrado, calcular a área e o perímetro do mesmo.'''

lado = int(input("Informe o tamanho de um dos lados do quadrado"))

area = lado * lado
perimetro = lado * 4

print("A área do quadrado é: ", area)
print("O perimetro do quadrado é: ", perimetro)

